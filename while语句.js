// while循环语句格式
/**
 *      初始化的变量
 *          while(条件) {
 *              执行的代码
 *              变量的变化
 *          }
 */

var a = 0;
while (a < 10) {
    console.log("111");
    a++;
}

var m = 60;
do {
    console.log("111");
} while (m < 10);
/**
 * 初始化变量
 * do{
 *      执行的代码
 *      变量的变化
 *  }while(条件)
 *
 * 注意:do while至少执行一次
 */

continue; // 跳出当前次执行

break; // 中断后续所有循环
